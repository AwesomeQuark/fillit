#include "../include/fillit.h"

void	ft_free(char ***data)
{
	int i;
	int *ptr;

	i = 0;
	ptr = data[0];
	while (data[i] != NULL)
	{
		ft_strdel(&data[i][0]);
		i++;
	}
	free(&ptr);
}
