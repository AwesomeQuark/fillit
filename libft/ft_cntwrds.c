int		ft_cntwrds(char const *s, char c)
{
	int i;
	int l;

	i = 0;
	l = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
		{
			while (s[i] != c && s[i] != '\0')
				i++;
			l++;
		}
		while ((s[i] == c) && (s[i] != '\0'))
			i++;
	}
	return (l);
}
