int		ft_atoi(const char *str)
{
	int i;
	int n;
	int r;

	i = 0;
	n = 1;
	r = 0;
	while ((str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f'
				|| str[i] == '\r' || str[i] == ' ') && str[i] != '\0')
		i++;
	if (((str[i] > 47 && str[i] < 58)) || (str[i] == 43 || str[i] == 45))
	{
		if ((str[i] == 43) || (str[i] == 45))
		{
			if (str[i] == 45)
				n = -1;
			i++;
		}
		while ((str[i] > 47 && str[i] < 58) && str[i] != '\0')
			r = (r * 10) + (str[i++] - 48);
		return (r * n);
	}
	return (0);
}
